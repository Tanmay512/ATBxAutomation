package Ex_ForLoop;

public class Ex_PrintJanToDec {
    static void main(String[] args) {
        String[] months ={"Jan","Feb", "Mar","Apr","May","Jun", "Jul","Aug","Sept","Oct","Nov","Dec"};

        for (int i=0;i< months.length;i++){
            System.out.println(months[i]);
        }
    }
}
